def get_icon(name: str) -> str:
    """
    Used to safely acquire icons from the data class
    """
    if not name: return Icons.unknown
    icon_name = name.lower()
    icon = getattr(Icons, icon_name) if hasattr(Icons, icon_name) else Icons.unknown
    return icon

